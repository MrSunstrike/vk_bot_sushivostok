import vk_api
from vk_api.longpoll import VkLongPoll, VkEventType
from dotenv import load_dotenv
from os import getenv
import random

load_dotenv()

TOKEN = getenv("TOKEN")

win_words = ["–≤—ã–π–≥—Ä", "–≤—ã–∏–≥—Ä–∞", "–ø–æ–±–µ–¥–∏–ª", "–ø—Ä–∏–∑"]
ny_words = ["–ø—Ä–µ–¥–∑–∞–∫–∞–∑", "–Ω–æ–≤—ã–π –≥–æ–¥"]


